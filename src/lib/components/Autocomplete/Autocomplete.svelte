input[type='text'] {
	background-color: #f1f1f1;
	width: 100%;
}
input[type='submit'] {
	background-color: DodgerBlue;
	color: #fff;
}

#autocomplete-items-list {
	position: relative;
	margin: 0;
	padding: 0;
	top: 0;
	width: 297px;
	border: 1px solid #ddd;
	background-color: #ddd;
}
